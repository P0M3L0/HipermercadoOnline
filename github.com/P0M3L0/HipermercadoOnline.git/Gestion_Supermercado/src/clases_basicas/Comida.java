package clases_basicas;

public class Comida {

	public int kcal;
	public int precio_ud;
	public boolean es_fresco;
	public String nombre_comida;
	
	public Comida (int kcal, int precio_ud, boolean es_fresco, String nombre_comida) {
		this.kcal = kcal;
		this.precio_ud = precio_ud;
		this.es_fresco = es_fresco;
		this.nombre_comida = nombre_comida;
		
	}
	
}
